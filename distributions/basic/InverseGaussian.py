from scipy.stats import wald


def create_p(pm=None):
    if pm == None:
        pm = {}
    return {
        'mu': pm.get('mu', 0.),
        'lambda': pm.get('lambda', 1.)
        }


def dump_p(pm):
    return {
        'mu': pm['mu'],
        'lambda': pm['lambda']
        }


def sample_data(pm, N):
    return wald.rvs(pm['mu'], pm['lambda'], size=N)


def data_prob(pm, y):
    return wald.pdf(y, pm['mu'], pm['lambda'])
